def bigest_number(first_number, second_number):
    if first_number > second_number:
        print("El numero mayor es el primero")
    else:
        print("El numero mayor es el segundo")


first_number = float(input("Escoja su primer numero"))
second_number = float(input("Escoja su segundo numero"))
bigest_number(first_number, second_number)
